package main.java;

public class Endoll {
    private final int id;
    private final float potencia;

    public Endoll(int id, float potencia) {
        this.id = id;
        this.potencia = potencia;
    }

    public int getId() {
        return id;
    }

    public float getPotencia() {
        return potencia;
    }

}
